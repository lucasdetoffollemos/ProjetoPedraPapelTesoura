    private void popularComboBoxIdioma(){
        ArrayList<String> idiomas = new ArrayList<>();
        idiomas.add(traducoes.getString("ESCOLHA_IDIOMA"));
        idiomas.add(traducoes.getString("PORTUGUES"));
        idiomas.add(traducoes.getString("INGLES"));
        idiomas.add(traducoes.getString("ESPANHOL"));
        
        for (String idioma : idiomas) {
            comboBoxLinguagem.addItem(idioma);
        }
    }

