    private void btJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btJogarActionPerformed
        JogadasSistema jogo = new JogadasSistema();
        
        String[] array = jogo.Jogar(respostaJogador);
        
        if(array != null){
            String resultadoJogo = array[0];
        
            String valorDoSistema = array[1];

            if(resultadoJogo != null){
                jLResultadoJogo.setText(resultadoJogo);
            }

            if(valorDoSistema!= null){
                tfResultadoSistema.setVisible(true);
                tfResultadoSistema.setText(valorDoSistema);
            }
        }
    }//GEN-LAST:event_btJogarActionPerformed

    private void comboBoxLinguagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxLinguagemActionPerformed
        if(comboBoxLinguagem.getSelectedIndex() == 1){
            idioma = new String("pt");
            pais = new String("BR");
            
            this.dispose();
            MenuJFrame frame = new MenuJFrame(" ");
            frame.setVisible(true);
        }
        else if(comboBoxLinguagem.getSelectedIndex() == 2){
            idioma = new String("en");
            pais = new String("US");
            
            this.dispose();
            MenuJFrame frame = new MenuJFrame(" ");
            frame.setVisible(true);
        }
        else if(comboBoxLinguagem.getSelectedIndex() == 3){
            idioma = new String("es");
            pais = new String("ES");
            
            this.dispose();
            MenuJFrame frame = new MenuJFrame(" ");
            frame.setVisible(true);
        }
        
    }//GEN-LAST:event_comboBoxLinguagemActionPerformed

